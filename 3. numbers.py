import math
# Imports all python math functions. its better to use this way "import math" instead of "from math import *", 

print(2)
# Prints a number on the screen

print(3 + 4.5)
# Prints out the result of adding the 2 numbers. You can also do subtract/multiple/divide

print(3 * (4 + 5))
# Uses parenthesis. The numbers inside the parenthesis are always calculated first. 

print(10 % 3)
# Modulist operation. This divides 10 by 3, then outputs the remainder

mu_num =5
print(mu_num)
